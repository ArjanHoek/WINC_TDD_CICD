const isAnagram = require("./isAnagram");

test("isAnagram is a function type", () => {
  expect(typeof isAnagram).toEqual("function");
});

test('String is anagram 1', () => {
  const string1 = "Tablet";
  const string2 = "Battle"
  expect(isAnagram(string1, string2)).toBeTruthy()
});

test('String is anagram 2', () => {
  const string1 = "Hi there";
  const string2 = "Thy here"
  expect(isAnagram(string1, string2)).toBeFalsy()
});

